    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String categoryID = jTextField1.getText();
        try {
            Connection con = project.connectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs1 = st.executeQuery("SELECT category.C_ID, category.CategoryName, product.ProductID, product.ProductName FROM category INNER JOIN product ON category.C_ID = product.C_ID WHERE category.C_ID = '" + categoryID + "'");
            jTable1.setModel(DbUtils.resultSetToTableModel(rs1));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Connection Error");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
